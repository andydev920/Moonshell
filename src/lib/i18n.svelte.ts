const messages: Record<Locale, Dict> = { zh, en, ja, fr, de, es };

const SUPPORTED: Locale[] = ["zh", "ja", "en", "fr", "de", "es"];

/** Browser/system language -> supported Locale; falls back to English. */
function detect(): Locale {
  if (typeof navigator === "undefined") return "en";
  for (const raw of navigator.languages ?? [navigator.language]) {
    const lc = (raw ?? "").toLowerCase();
    if (lc.startsWith("zh")) return "zh";
    if (lc.startsWith("ja")) return "ja";
    if (lc.startsWith("fr")) return "fr";
    if (lc.startsWith("de")) return "de";
    if (lc.startsWith("es")) return "es";
    if (lc.startsWith("en")) return "en";
  }
  return "en";
}

/** User preference: a specific Locale or "auto" (follow system). */
export type Pref = Locale | "auto";

function initialPref(): Pref {
  try {
    const saved = localStorage.getItem(STORE_KEY);
    if (saved === "auto") return "auto";
    if (saved && SUPPORTED.includes(saved as Locale)) return saved as Locale;
  } catch {
    // localStorage unavailable (e.g. private mode): use default
  }
  return "auto"; // default: follow system
}

class I18n {
  /** User choice: a Locale or "auto". This is what gets persisted. */
  pref = $state<Pref>(initialPref());
  /** Effective locale: detected system language when "auto", else pref. */
  locale = $derived<Locale>(this.pref === "auto" ? detect() : this.pref);

  set(p: Pref) {
    if (p !== "auto" && !SUPPORTED.includes(p)) return;
    this.pref = p;
    try {
      localStorage.setItem(STORE_KEY, p);
    } catch {
      // ignore write failures; applies for this session at least
    }
  }

  /** Look up a string; falls back to English then the key. {xxx} replaced from params. */
  t(key: string, params?: Record<string, string | number>): string {
    const dict = messages[this.locale] ?? en;
    let s = dict[key] ?? en[key] ?? key;
    if (params) {
      for (const k in params) {
        s = s.split(`{${k}}`).join(String(params[k]));
      }
    }
    return s;
  }
}

export const i18n = new I18n();

/** Template convenience wrapper; reads i18n.locale so views re-render on switch. */
export function t(
  key: string,
  params?: Record<string, string | number>,
): string {
  return i18n.t(key, params);
}
